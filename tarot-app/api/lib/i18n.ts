// 语言检测
export function detectLang(req: any): string {
  const cookieLang = req.headers.cookie?.split(';').find((c: string) => c.trim().startsWith('i18nLang='))?.split('=')[1]?.trim();
  if (cookieLang) return cookieLang;
  const acceptLang = req.headers['accept-language'] || '';
  if (acceptLang.includes('zh-TW') || acceptLang.includes('zh-Hant')) return 'zh-TW';
  if (acceptLang.includes('zh')) return 'zh-CN';
  if (acceptLang.includes('ja')) return 'ja';
  if (acceptLang.includes('ko')) return 'ko';
  return 'en';
}

// 获取语言代码
export function getLang(req: any): string {
  const lang = detectLang(req);
  return (lang === 'zh-TW' || lang === 'en' || lang === 'ja' || lang === 'ko') ? lang : 'zh-CN';
}

// 翻译函数
export function t(req: any, key: keyof typeof MSG['zh-CN'], ...args: any[]): string {
  const lang = detectLang(req);
  const langKey = lang === 'zh-TW' ? 'zh-TW' : lang === 'en' ? 'en' : lang === 'ja' ? 'ja' : lang === 'ko' ? 'ko' : 'zh-CN';
  const msg = MSG[langKey as keyof typeof MSG] || MSG['zh-CN'];
  const val = msg[key];
  return typeof val === 'function' ? (val as Function)(...args) : val;
}
